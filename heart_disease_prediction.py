import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score
import warnings
warnings.filterwarnings(
    "ignore",
    message="X does not have valid feature names"
)

"""Data Collection and Processing"""

# Loading the data from csv file type to Pandas Data Frame
heart_data = pd.read_csv('/content/heart_disease_data.csv')

# Print the first 5 rows of the data set
heart_data.head()

# Print the last 5 rows of the data set
heart_data.tail()

# Check the number of columns in the dataset
heart_data.shape

# Getting information about the data
heart_data.info()

# Check for missing values
heart_data.isnull().sum()

# Statistics for data
heart_data.describe()

# Check distribution of target variable
heart_data['target'].value_counts()

"""1 -> Unhealthy Heart (Positive Heart Disease)

0 -> Healthy Heart (Negative Heart Disease)

Splitting the Characteristics and Target
"""

X = heart_data.drop(columns='target', axis=1)
Y = heart_data['target']

print(X)

print(Y)

"""Splitting the Data into Training data and Test Data"""

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.2,stratify = Y, random_state = 5)

print(X.shape,X_train.shape,X_test.shape)

"""Model Training using Logistic Regression"""

model = LogisticRegression()

# Training Logistic Regression model w/ training data
model.fit(X_train, Y_train)

"""Model Evaluation/Accuracy Store"""

# Accuracy on training data
X_train_prediction = model.predict(X_train)
training_data_accuracy = accuracy_score(X_train_prediction, Y_train)

print('Accuracy on training data: ',training_data_accuracy)

# Accuracy on test data
X_test_prediction = model.predict(X_test)
test_data_accuracy = accuracy_score(X_test_prediction, Y_test)

print('Accuracy on test data: ',test_data_accuracy)

"""Building a Predictive System"""

# Sample User Data: 52,1,3,152,298,1,1,178,0,1.2,1,0,3,1
user_data = (52,1,3,152,298,1,1,178,0,1.2,1,0,3)
# Convert to numpy array
user_data_nparr = np.asarray(user_data)
# Reshape the numpy array since we are predicting for a single instance
user_data_reshaped = user_data_nparr.reshape(1,-1)
# Predict
prediction = model.predict(user_data_reshaped)
print(prediction)
# Display message for user
if prediction[0] == 0:
  print('The person does not have a heart disease')
else:
  print('The person has a heart disease')